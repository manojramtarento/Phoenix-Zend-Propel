        }
        if ($this->isColumnModified(RCustomActivitesPeer::ORD)) {
            $modifiedColumns[':p' . $index++]  = '`ord`';
        }

        $sql = sprintf(
            'INSERT INTO `r_custom_activites` (%s) VALUES (%s)',
            implode(', ', $modifiedColumns),
            implode(', ', array_keys($modifiedColumns))
        );

        try {
            $stmt = $con->prepare($sql);
            foreach ($modifiedColumns as $identifier => $columnName) {
                switch ($columnName) {
                    case '`act_id`':
                        $stmt->bindValue($identifier, $this->act_id, PDO::PARAM_INT);
                        break;
                    case '`act_libelle`':
                        $stmt->bindValue($identifier, $this->act_libelle, PDO::PARAM_STR);
                        break;
                    case '`actif`':
                        $stmt->bindValue($identifier, (int) $this->actif, PDO::PARAM_INT);
                        break;
                    case '`ord`':
                        $stmt->bindValue($identifier, $this->ord, PDO::PARAM_INT);
                        break;
                }
            }
            $stmt->execute();
        } catch (Exception $e) {
            Propel::log($e->getMessage(), Propel::LOG_ERR);
            throw new PropelException(sprintf('Unable to execute INSERT statement [%s]', $sql), $e);
        }

        $this->setNew(false);
    }

